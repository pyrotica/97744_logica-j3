function somar(a, b){
    return a +b
}

function subtr(a,b){
    return a-b
}
function div(a,b){
    return a/b
}
function mult(a,b){
    return a*b
}

const soma = somar(2, 5)
const subtrair = subtr(2, 5)
const divisao = div(2, 5)
const multiplicacao = mult(2, 5)

console.log("soma: "+ soma)
console.log("subtração: "+ subtrair)
console.log("multiplicação: " + multiplicacao)
console.log("divisão: " + divisao)